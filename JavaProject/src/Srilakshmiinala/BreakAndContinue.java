package Srilakshmiinala;

public class BreakAndContinue {
public static void main(String[] args) {
	

for(int i=1;i<=5;i++)
{
	if(i==4)
	{
		break;
	}
	
	System.out.println(i+" " + "Livetech");
}
System.out.println("****************");
for(int i=1;i<=7;i++)
{
	if(i==4)
	{
		continue;
	}

System.out.println(i+" " + "Livetech");
}
}
}
